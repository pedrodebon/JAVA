

public class Ejercicio10 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		int resultado = num1 - num2;
		System.out.println("10 - 5 = " + resultado);
		
		num1 = 55;
		num2 = 45;
		resultado = num1 + num2;
		System.out.println("55 + 45 = " + resultado);
		
		num1 = 3;
		resultado = -num1;
		System.out.println("3 Negativo: " + resultado);
		
		num1 = 5;
		num2 = 5;
		resultado = num1 * num2;
		System.out.println("5 * 5 = " + resultado);
		
		num1 = 25;
		num2 = 7;
		resultado = num1 / num2;
		System.out.println("Entero: 25 / 7 = " + resultado);
		
		float fnum1 = 25;
		float fnum2 = 7;
		float fresultado = fnum1 / fnum2;
		System.out.println("Real: 25 / 7 = " + fresultado);
		
		num1 = 25;
		num2 = 4;
		resultado = num1 % num2;
		System.out.println("25 % 4 = " + resultado);
	}
}
