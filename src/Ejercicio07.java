

public class Ejercicio07 {
	public static void main(String[] args) {
		// Lógicos: boolean
		final boolean aux1 = true;
		System.out.println("Variable del tipo boolean tipo lógica con valor: " + aux1);
		// Caracter: char
		final char aux2 = '&';
		System.out.println("Variable del tipo char tipo character con valor: " + aux2);
		// Numéricos enteros: byte, short, int, long
		final byte aux3 = 100;
		System.out.println("Variable del tipo byte tipo numérico entero con valor: " + aux3);
		final short aux4 = 10000;
		System.out.println("Variable del tipo short tipo numérico entero con valor: " + aux4);
		final int aux5 = 1000000;
		System.out.println("Variable del tipo int tipo numérico entero con valor: " + aux5);
		final long aux6 = 1000000000;
		System.out.println("Variable del tipo long tipo numérico entero con valor: " + aux6);
		// Numéricos reales: float. double
		final float aux7 = 123.456f;
		System.out.println("Variable del tipo float tipo numérico real con valor: " + aux7);
		final double aux8 = 123456.7809;
		System.out.println("Variable del tipo double tipo numérico real con valor: " + aux8);
	}
}
