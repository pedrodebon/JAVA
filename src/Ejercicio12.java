

public class Ejercicio12 {

	public static void main(String[] args) {
		int resultado = 25;
		resultado += 5 ;
		System.out.println("1. Resultado + 5 = " +  resultado); 
		
		resultado -= 15;
		System.out.println("2. Resultado - 15 = " +  resultado); 
		
		resultado *= 2;
		System.out.println("3. Resultado * 2 = " +  resultado); 
		
		resultado /= 2;
		System.out.println("4. Resultado / 2 = " +  resultado); 
		
		resultado %= 3;
		System.out.println("5. Resultado % 3 = " +  resultado); 
	}
}
